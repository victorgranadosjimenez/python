
def add(a, b):
    result = a + b
    return result
def mul(a, b):
    result = a * b
    return result
def div(a, b):
    result = a / b
    return result
def ded(a, b):
    result = a - b
    return result
def advanced_calcultor(weight, height):
    result = round(weight / (height * height), 2)
    return result

